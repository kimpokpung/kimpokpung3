package cleats;

public class Cleats {
	private Integer num;
	private String brand;
	private String name;
	private String silo;
	private Integer price;
	private String desc;
	private String pic;
	public Integer getNum() {		return num;	} public void setNum(Integer num) {		this.num = num;	}
	public String getBrand() {		return brand;	} public void setBrand(String brand) {		this.brand = brand;	}
	public String getName() {		return name;	} public void setName(String name) {		this.name = name;	}
	public String getSilo() {		return silo;	} public void setSilo(String silo) {		this.silo = silo;	}
	public Integer getPrice() {		return price;	} public void setPrice(Integer price) {		this.price = price;	}
	public String getDesc() {		return desc;	} public void setDesc(String desc) {		this.desc = desc;	}
	public String getPictrue() {		return pic;	} public void setPictrue(String pictrue) {		this.pic = pictrue;	}
}
